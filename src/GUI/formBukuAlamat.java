    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
         DefaultTableModel model = (DefaultTableModel)tabel.getModel();
         txtnama.setText(model.getValueAt(tabel.getSelectedRow(), 0).toString());
         txtnpm.setText(model.getValueAt(tabel.getSelectedRow(), 1).toString());
         jeniskelamin.setSelectedItem(model.getValueAt(tabel.getSelectedRow(), 2).toString());
         txtalamat.setText(model.getValueAt(tabel.getSelectedRow(), 3).toString());
    }//GEN-LAST:event_tabelMouseClicked

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tabel.getModel();
        if(tabel.getSelectedRow()==-1){
            if(tabel.getRowCount()==0){
                JOptionPane.showMessageDialog(null, "Tidak ada data dalam tabel");
                
            }else{
                JOptionPane.showMessageDialog(null, "pilih data yang akan dihapus..!!");
            }
        }else{
            model.removeRow(tabel.getSelectedRow());
        }
        reset();
    }//GEN-LAST:event_btnhapusActionPerformed

