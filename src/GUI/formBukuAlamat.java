    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tabel.getModel();
        
        if (txtnama.getText().trim().equals("")||txtnpm.getText().trim().equals("")||txtalamat.getText().trim().equals("")|| jeniskelamin.getSelectedItem().equals("") ){
            JOptionPane.showMessageDialog(null, "Data Yang Anda Masukkan Belum Lengkap\nSilahkan Ulangi lagi");
        }else{
            model.addRow(new Object[]{txtnama.getText(), txtnpm.getText(), jeniskelamin.getSelectedItem(), txtalamat.getText()});
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        }
        reset();
        
        
        
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        // TODO add your handling code here:
         DefaultTableModel model = (DefaultTableModel)tabel.getModel();
         if(tabel.getSelectedRow()==-1){
              if(tabel.getRowCount()==0){
                  JOptionPane.showMessageDialog(null, "Tidak Ada Data Dalam Tabel");
              }else{
                  JOptionPane.showMessageDialog(null, "Pilih data yang akan diubah");
              }
         }else{
             model.setValueAt(txtnama.getText(), tabel.getSelectedRow(), 0);
             model.setValueAt(txtnpm.getText(), tabel.getSelectedRow(), 1);
             model.setValueAt(jeniskelamin.getSelectedItem() , tabel.getSelectedRow(), 2);
             model.setValueAt(txtalamat.getText(), tabel.getSelectedRow(), 3);
         }
         reset();
        
    }//GEN-LAST:event_btnubahActionPerformed

